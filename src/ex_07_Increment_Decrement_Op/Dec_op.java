package ex_07_Increment_Decrement_Op;

public class Dec_op {
    public static void main(String[] args) {
        int a = 10;
        int result_post = a--;
        System.out.println(result_post); // 10
        System.out.println(a); //9

        int b= 10;
        int result = --b;
        System.out.println(result);
        System.out.println(b);
        // sumOfTwo(3,4);
    }
}
