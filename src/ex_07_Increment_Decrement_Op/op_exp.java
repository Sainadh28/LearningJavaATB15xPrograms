package ex_07_Increment_Decrement_Op;

public class op_exp {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a;
        System.out.println(result);

        int b = 10;
        int result1 = a++;
        System.out.println(b);
        System.out.println(result1);
    }
}
